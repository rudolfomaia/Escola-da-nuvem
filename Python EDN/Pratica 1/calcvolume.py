# Esse código tem como objetivo calcular o volume de um objeto

# Vars das medidas
comprimento = 12
largura = 14
altura = 20

# Var de cálculo
volume = comprimento * largura * altura

# Print do resultado
print(f"O volume da caixa é a multipliacação de comprimento ({comprimento}) , largura ({largura}) e altura ({altura}), resultando em seu volume ({volume}) em cm³" )